# Python simple data types
# Если вы хотите прокомментировать нашу строку в Python, просто используйте '#'
print(500)

# Чтобы заказать печать чего-либо в используемом консольном методе print ()
print(550)

# Метод типа используется для получения дополнительной информации об объекте
print(help(500))


# Переменные (Variable)
# 'variable_name' = 'variable_value'
a = 500
print(a)


# Simple data types (Простые типы данных)
# Integer - full number (Целое - полное число)
a =500
print(a)
print(type(a))


# Float - decimal point number (Float - число десятичной точки)
a = 50.0
print(a)
print(type(a))

# String - text
a = 'Hello world'
print(a)
print(type(a))

# Boolean - True or False (1 or 10) (Логическое значение - Истина или Ложь (1 или 10))
a = True
print(a)
print(type(a))
